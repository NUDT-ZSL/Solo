export const MAX_AGE = 14 * BILLION;

export function temperatureToColor(temperature: number): string {
  if (temperature >= 30000) return '#A0C4FF';
  if (temperature >= 10000) return '#CADCFF';
  if (temperature >= 7500) return '#F0F0FF';
  if (temperature >= 6000) return '#FFD700';
  if (temperature >= 5000) return '#FFA500';
  if (temperature >= 3500) return '#FF6347';
  return '#FF4500';
}

export function formatAge(age: number): string {
  if (age >= 1 * BILLION) return `${(age / BILLION).toFixed(2)} 十亿年`;
  if (age >= 1 * MILLION) return `${(age / MILLION).toFixed(2)} 百万年`;
  if (age >= 1000) return `${(age / 1000).toFixed(2)} 千年`;
  return `${age.toFixed(0)} 年`;
}

export function formatNumber(num: number): string {
  if (num >= 1e9) return `${(num / 1e9).toFixed(2)} × 10⁹`;
  if (num >= 1e6) return `${(num / 1e6).toFixed(2)} × 10⁶`;
  if (num >= 1e3) return `${(num / 1e3).toFixed(2)} × 10³`;
  if (num < 0.01 && num > 0) return num.toExponential(2);
  return num.toFixed(2);
}
