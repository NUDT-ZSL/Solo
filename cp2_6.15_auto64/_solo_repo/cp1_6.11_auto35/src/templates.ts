export function getCharTemplate(char: string): PixelTemplate {
  const upperChar = char.toUpperCase();
  if (charTemplates[upperChar]) {
    return charTemplates[upperChar];
  }
  return squareTemplate;
}

export interface PixelColorData {
  isOddAscii: boolean;
  mixRatio: number;
}

export interface PixelGrid {
  width: number;
  height: number;
  pixels: {
    x: number;
    y: number;
    filled: boolean;
    charIndex: number;
    charCode: number;
    colorData: PixelColorData;
  }[];
}

export function getPixelColorData(charCode: number): PixelColorData {
  const isOdd = charCode % 2 === 1;
  return {
    isOddAscii: isOdd,
    mixRatio: isOdd ? 0.7 : 0.9,
  };
}

export function wordToPixelGrid(word: string): PixelGrid {
  const chars = word.split('');
  const charCount = chars.length;
  const gridWidth = charCount * 8 + (charCount - 1) * 1;
  const gridHeight = 8;
  const pixels: PixelGrid['pixels'] = [];

  for (let charIdx = 0; charIdx < charCount; charIdx++) {
    const char = chars[charIdx];
    const template = getCharTemplate(char);
    const charCode = char.charCodeAt(0);
    const colorData = getPixelColorData(charCode);
    const offsetX = charIdx * 9;

    for (let row = 0; row < 8; row++) {
      for (let col = 0; col < 8; col++) {
        const x = offsetX + col;
        const y = row;
        pixels.push({
          x,
          y,
          filled: template[row][col],
          charIndex: charIdx,
          charCode,
          colorData,
        });
      }
    }
  }

  return { width: gridWidth, height: gridHeight, pixels };
}
