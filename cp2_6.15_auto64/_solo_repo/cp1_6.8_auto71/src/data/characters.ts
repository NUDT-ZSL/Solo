export type CalligraphyStyle = 'kaishu' | 'xingshu' | 'caoshu';

export interface StrokeData {
  name: string;
  points: [number, number][];
  widths: number[];
}

export interface CharacterVariant {
  style: CalligraphyStyle;
  strokes: StrokeData[];
  bounds: { width: number; height: number };
}

export interface CharacterData {
  char: string;
  variants: CharacterVariant[];
  pinyin: string;
  strokeCount: number;
  meaning: string;
}

function makeStroke(
  name: string,
  points: [number, number][],
  widths: number[]
): StrokeData {
  return { name, points, widths };
}

function shiftPoints(
  points: [number, number][],
  dx: number,
  dy: number
): [number, number][] {
  return points.map(([x, y]) => [x + dx, y + dy] as [number, number]);
}

function curvePoints(
  points: [number, number][],
  wobble: number
): [number, number][] {
  return points.map(([x, y], i) => {
    if (i === 0 || i === points.length - 1) return [x, y] as [number, number];
    const wx = (Math.sin(i * 2.7 + x * 10) * wobble);
    const wy = (Math.cos(i * 3.1 + y * 10) * wobble);
    return [x + wx, y + wy] as [number, number];
  });
}

function createXingshuVariant(kaishu: StrokeData[]): StrokeData[] {
  return kaishu.map((s, i) => {
    const wobble = 0.015;
    const shifted = shiftPoints(s.points, (i % 2 === 0 ? 1 : -1) * 0.01, 0.005);
    const curved = curvePoints(shifted, wobble);
    const widerWidths = s.widths.map(w => w * (0.85 + Math.random() * 0.3));
    return makeStroke(s.name, curved, widerWidths);
  });
}

function createCaoshuVariant(kaishu: StrokeData[]): StrokeData[] {
  const merged: StrokeData[] = [];
  let skip = false;
  for (let i = 0; i < kaishu.length; i++) {
    if (skip) { skip = false; continue; }
    const s = kaishu[i];
    const curved = curvePoints(
      shiftPoints(s.points, (Math.random() - 0.5) * 0.03, (Math.random() - 0.5) * 0.03),
      0.03
    );
    if (i < kaishu.length - 1 && Math.random() > 0.5) {
      const next = kaishu[i + 1];
      const connected: [number, number][] = [
        ...curved,
        [curved[curved.length - 1][0] * 0.5 + next.points[0][0] * 0.5,
         curved[curved.length - 1][1] * 0.5 + next.points[0][1] * 0.5] as [number, number],
        ...shiftPoints(next.points, (Math.random() - 0.5) * 0.02, (Math.random() - 0.5) * 0.02),
      ];
      const connectedWidths = [
        ...s.widths,
        Math.min(s.widths[s.widths.length - 1], next.widths[0]) * 0.5,
        ...next.widths,
      ];
      merged.push(makeStroke(s.name + '·' + next.name, connected, connectedWidths));
      skip = true;
    } else {
      merged.push(makeStroke(s.name, curved, s.widths.map(w => w * (0.7 + Math.random() * 0.5))));
    }
  }
  return merged;
}

function createCharacter(
  char: string,
  pinyin: string,
  meaning: string,
  kaishuStrokes: StrokeData[]
): CharacterData {
  return {
    char,
    pinyin,
    meaning,
    strokeCount: kaishuStrokes.length,
    variants: [
      {
        style: 'kaishu',
        strokes: kaishuStrokes,
        bounds: { width: 1, height: 1 },
      },
      {
        style: 'xingshu',
        strokes: createXingshuVariant(kaishuStrokes),
        bounds: { width: 1, height: 1 },
      },
      {
        style: 'caoshu',
        strokes: createCaoshuVariant(kaishuStrokes),
        bounds: { width: 1, height: 1 },
      },
    ],
  };
}

