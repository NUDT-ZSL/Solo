def generate_poem(category: str, intensity: int) -> str:
    level = "low" if intensity <= 3 else ("mid" if intensity <= 7 else "high")
    poems = EMOTION_POEMS.get(category, EMOTION_POEMS["calm"]).get(level, EMOTION_POEMS["calm"]["mid"])
    return random.choice(poems)
