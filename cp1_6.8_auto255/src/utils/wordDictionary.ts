const wordSet = new Set(WORDS)

const charIndex = new Map<string, Set<string>>()
WORDS.forEach(word => {
  const firstChar = word[0]
  if (!charIndex.has(firstChar)) {
    charIndex.set(firstChar, new Set())
  }
  charIndex.get(firstChar)!.add(word)
})

export function isValidWord(word: string): boolean {
  return wordSet.has(word)
}

export function getWordsByFirstChar(char: string): string[] {
  return Array.from(charIndex.get(char) || [])
}

export function getRandomStartWord(): string {
  const goodStarts = WORDS.filter(w => {
    const lastChar = w[w.length - 1]
    return charIndex.has(lastChar) && charIndex.get(lastChar)!.size >= 3
  })
  return goodStarts[Math.floor(Math.random() * goodStarts.length)]
}

export function hasWordsStartingWith(char: string): boolean {
  return charIndex.has(char) && charIndex.get(char)!.size > 0
}

export function isWordUsed(word: string, usedWords: string[]): boolean {
  return usedWords.includes(word)
}
