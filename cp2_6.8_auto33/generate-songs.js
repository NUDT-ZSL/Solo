function generateSong(id, emotion, songIndex) {
  const data = emotionSongData[emotion];
  const title = data.titles[songIndex % data.titles.length];
  const artist = data.artists[songIndex % data.artists.length];
  
  const lyrics = [];
  let currentTime = 0;
  
  const baseLyrics = [...data.lyrics];
  const extraLyrics = [...extraLyricsPool[emotion]];
  
  for (let i = 0; i < 12; i++) {
    let text;
    if (i < baseLyrics.length) {
      text = baseLyrics[i];
    } else {
      text = extraLyrics[(i - baseLyrics.length) % extraLyrics.length];
    }
    lyrics.push({
      time: currentTime,
      text: text
    });
    currentTime += 5 + Math.floor(Math.random() * 3);
  }
  
  return {
    id: `song_${emotion}_${id}`,
    title: title,
    artist: artist,
    cover: `https://picsum.photos/seed/${emotion}${id}/300/300`,
    emotion: emotion,
    lyrics: lyrics
  };
}

const songs = [];
let songId = 1;

for (const emotion of emotions) {
  for (let i = 0; i < 20; i++) {
    songs.push(generateSong(songId, emotion, i));
    songId++;
  }
}

